package tools;

public class RunningTime {

    private int vertex;
    private int edge;
    private long time;

    public long getTime() {
        return time;
    }

    public void setTime(long time) {
        this.time = time;
    }

    public int getEdge() {
        return edge;
    }

    public void setEdge(int edge) {
        this.edge = edge;
    }

    public int getVertex(){
        return vertex;
    }
    public void setVertex(int vertex) {
        this.vertex = vertex;
    }

}
